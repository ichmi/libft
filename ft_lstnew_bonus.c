#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = (t_list *)malloc(sizeof(*node));
	if (!node)
		return (NULL);
	node->content = content; // munmap_chunk(): invalid pointer, solves w/ ft_strdup(content);
	node->next = NULL;
	return (node);
}
